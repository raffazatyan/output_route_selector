/// Enum representing different types of audio output devices.
enum AudioDeviceType {
  /// Built-in speaker.
  speaker,

  /// Built-in receiver (earpiece on iPhone).
  receiver,

  /// Wired headset or headphones.
  wiredHeadset,

  /// Bluetooth audio device (non-AirPods).
  bluetooth,

  /// Apple AirPods (any model).
  airpods,
}
